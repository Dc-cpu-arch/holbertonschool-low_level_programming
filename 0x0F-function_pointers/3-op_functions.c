/**
 * op_add - sums two integers
 * @a: first int
 * @b: second int
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two integers
 * @a: first int
 * @b: second int
 *
 * Return: substract of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - gives the product of two integers
 * @a: first int
 * @b: second int
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first int
 * @b: second int
 *
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gives the module between two integers
 * @a: first int
 * @b: second int
 *
 * Return: module between a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
